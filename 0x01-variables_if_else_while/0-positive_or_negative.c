/**
 * main - Entry point
 *
 * This function executes the main logic of the program and returns an
 * integer value to the operating system indicating whether the program
 * was executed successfully or not.
 *
 * Return: Always return 0 (success)
 */
int main(void)
{
	int n;

	srand(time(0));
	n = rand() - RAND_MAX / 2;
	/* your code goes there */
	if (n > 0)
		printf("%d is positive\n", n);
	else if (n == 0)
		printf("%d is zero\n", n);
	else
		printf("%d is negative\n", n);
	return (0);
}
